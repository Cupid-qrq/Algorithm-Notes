// AC方法：破环成链 + 区间DP

#include <bits/stdc++.h>
using namespace std;

/* ==================== 问题建模 ====================
 *
 * 【珠子表示】
 * 输入：n 个数，第 i 个数表示第 i 颗珠子的头标记
 * 第 i 颗珠子：头 = a[i]，尾 = a[i+1]（注意：第n颗珠子尾 = a[1]）
 *
 * 【合并操作】
 * 合并珠子 i 和珠子 i+1：
 * - 释放能量：head[i] × tail[i] × tail[i+1]
 * - 新珠子：头 = head[i]，尾 = tail[i+1]
 *
 * 【环形处理】
 * 项链是环形的，可以从任意位置断开
 * 技巧：破环成链，将数组复制一倍
 */

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int n;
  cin >> n;

  vector<int> a(2 * n + 1);

  // 读入并复制数组
  for (int i = 1; i <= n; i++) {
    cin >> a[i];
    a[i + n] = a[i]; // 复制一倍，破环成链
  }

  /* ==================== 区间DP ====================
   *
   * 状态定义：
   * dp[i][j] = 合并区间 [i, j] 内所有珠子能释放的最大能量
   *
   * 珠子定义：
   * 第 i 颗珠子：头 = a[i]，尾 = a[i+1]
   *
   * 状态转移：
   * 枚举分割点 k，将 [i, j] 分成 [i, k] 和 [k+1, j]
   * 合并后释放能量：a[i] × a[k+1] × a[j+1]
   *
   * dp[i][j] = max(dp[i][k] + dp[k+1][j] + a[i] * a[k+1] * a[j+1])
   *            其中 i <= k < j
   *
   * 初始化：
   * dp[i][i] = 0（单个珠子不能合并）
   *
   * 遍历顺序：
   * 按区间长度从小到大枚举
   */

  vector<vector<int>> dp(2 * n + 1, vector<int>(2 * n + 1, 0));

  // 枚举区间长度 len
  for (int len = 2; len <= n; len++) {
    // 枚举区间起点 i
    for (int i = 1; i + len - 1 <= 2 * n; i++) {
      int j = i + len - 1; // 区间终点

      // 枚举分割点 k
      for (int k = i; k < j; k++) {
        // 合并 [i, k] 和 [k+1, j]
        int energy = dp[i][k] + dp[k + 1][j] + a[i] * a[k + 1] * a[j + 1];
        dp[i][j] = max(dp[i][j], energy);
      }
    }
  }

  /* ==================== 答案统计 ====================
   *
   * 枚举所有长度为 n 的连续区间 [i, i+n-1]
   * 这些区间对应原环的所有切开方式
   */
  int ans = 0;
  for (int i = 1; i <= n; i++) {
    ans = max(ans, dp[i][i + n - 1]);
  }

  cout << ans << "\n";

  return 0;
}

/* ==================== 时间复杂度分析 ====================
 *
 * 三层循环：
 * - 外层：区间长度 O(n)
 * - 中层：区间起点 O(n)
 * - 内层：分割点 O(n)
 *
 * 总时间复杂度：O(n^3)
 * 空间复杂度：O(n^2)
 *
 * 数据范围：n <= 100，完全可以接受
 */

/* ==================== 样例模拟 ====================
 *
 * 输入：n = 4, a = [2, 3, 5, 10]
 *
 * 破环成链后：a = [2, 3, 5, 10, 2, 3, 5, 10]
 *
 * 珠子表示：
 * 珠子1: (2, 3)
 * 珠子2: (3, 5)
 * 珠子3: (5, 10)
 * 珠子4: (10, 2)
 *
 * 计算 dp[1][4]（从第1颗珠子到第4颗珠子）：
 *
 * len=2:
 * dp[1][2] = a[1] * a[2] * a[3] = 2 * 3 * 5 = 30
 * dp[2][3] = a[2] * a[3] * a[4] = 3 * 5 * 10 = 150
 * dp[3][4] = a[3] * a[4] * a[5] = 5 * 10 * 2 = 100
 * dp[4][5] = a[4] * a[5] * a[6] = 10 * 2 * 3 = 60
 *
 * len=3:
 * dp[1][3] = max(
 *   dp[1][1] + dp[2][3] + a[1]*a[2]*a[4] = 0 + 150 + 2*3*10 = 210,
 *   dp[1][2] + dp[3][3] + a[1]*a[3]*a[4] = 30 + 0 + 2*5*10 = 130
 * ) = 210
 *
 * len=4:
 * dp[1][4] = max(
 *   dp[1][1] + dp[2][4] + a[1]*a[2]*a[5] = ...,
 *   dp[1][2] + dp[3][4] + a[1]*a[3]*a[5] = ...,
 *   dp[1][3] + dp[4][4] + a[1]*a[4]*a[5] = 210 + 0 + 2*10*2 = 250
 * )
 *
 * 最终答案：max(dp[1][4], dp[2][5], dp[3][6], dp[4][7]) = 710
 */